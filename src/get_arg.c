#include "sh1.h"

char		**get_arg(char *buf)
{
	char	**arg;
	int		i;

	i = 0;
	while (buf[i])
	{
		if (buf[i] == '	')
			buf[i] = ' ';
		i++;
	}
	arg = ft_strsplit(buf, ' ');
	return (arg);
}
